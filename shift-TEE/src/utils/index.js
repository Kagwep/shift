import nodemailer from "nodemailer";


/**
 * Create email transporter using runtime settings
 */
export function createEmailTransporter(config) {
    const emailUser = config.emailOutgoingUser; 
    const emailPass = config.emailOutgoingPass;
    
    if (!emailUser || !emailPass) {
        throw new Error("EMAIL_USER and EMAIL_APP_PASSWORD must be set in runtime settings");
    }
    
    return nodemailer.createTransport({
        service: "gmail",
        host: "smtp.gmail.com",
        auth: {
            user: emailUser,
            pass: emailPass
        },
        secure: true,
        port: 465
    });
}

/**
 * Send email using runtime settings
 */
export async function sendEmail(
    config,
    options
) {
    const transporter = createEmailTransporter(config);
    const emailUser = config.emailUser;
    
    const mailOptions = {
        from: options.from || emailUser,
        to: options.to,
        subject: options.subject,
        html: options.html,
        text: options.text
    };
    
    try {
        const info = await transporter.sendMail(mailOptions);
        console.log("Email sent successfully:", info.messageId);
        return { success: true, messageId: info.messageId };
    } catch (error) {
        console.error("Failed to send email:", error);
        throw new Error(`Email sending failed: ${error.message}`);
    }
}

